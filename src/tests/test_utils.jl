using Test
include("../GraphCalculus.jl")
###########################################
# test graph_gradient
###########################################
f = [1,2,3,4]
Q =[0 1/2 0 1/2;
    1/3 0 1/3 1/3;
    0 1 0 0;
    1/2 1/2 0 0
]
∇(g) = graph_gradient(Q,g)
m = ∇(f)
∇f = [
        0. -1. 0. -3.;
        1. 0. -1. -2.;
        0. 1. 0. 0.;
        3. 2. 0. 0.
]
@test m ≈ ∇f

###########################################
# test graph_divergence
###########################################
Q =[0 1/2 0 1/2;
    1/3 0 1/3 1/3;
    0 1 0 0;
    1/2 1/2 0 0
]
m = [
    0 -1 0 -3;
    1 0 -1 -2;
    0 1 0 0;
    3 2 0 0;
]
f = [2 2/3 -1 -5/2]
div(m) = graph_divergence(Q, m)
divm = div(m)
@test f ≈ divm
